namespace Flight.Model;

/// <summary>
/// An Coordinates object.
/// </summary>
public class Coordinates
{
    internal Coordinates() { }

    /// <summary>
    /// Gets or sets the type of the x.
    /// </summary>
    /// <value>The type of the x.</value>
    public int X { get; set; }

    /// <summary>
    /// Gets or sets the type of the y.
    /// </summary>
    /// <value>The type of the y.</value>
    public int Y { get; set; }
}